const baseURL =
  "https://raw.githubusercontent.com/PokeAPI/sprites/master/sprites/pokemon/";

function criaTwet() {
  const tweet = document.createElement("div");
  tweet.setAttribute("class", "tweet");
  const topoTwet = document.createElement("div");
  topoTwet.setAttribute("class", "topoTwet");
  const imagem = document.createElement("img");
  imagem.setAttribute("class", "imagem");
  const nome = document.createElement("span");
  nome.setAttribute("class", "nome");
  const arroba = document.createElement("p");
  arroba.setAttribute("class", "arroba");
  const conteudo = document.createElement("p");
  conteudo.setAttribute("class", "conteudo");
  topoTwet.append(imagem, nome);
  tweet.append(topoTwet, arroba, conteudo);
  return tweet;
}

function escolheNum() {
  return Math.floor(Math.random() * 151);
}

const botao = document.querySelector("#formulario");
botao.addEventListener("submit", function (e) {
  e.preventDefault();
  const entrada = document.querySelector("#textoDoTweet");
  document.querySelector("#tweets").append(teste(entrada.value));
  entrada.value = "";
});

function teste(conteudo) {
  const elemento = criaTwet();
  elemento.querySelector(".conteudo").innerText = conteudo;
  const pokemonNum = escolheNum();
  const pokemonNome = nomePokemons[pokemonNum - 1];
  elemento
    .querySelector(".imagem")
    .setAttribute("src", `${baseURL}${pokemonNum}.png`);
  elemento.querySelector(".nome").innerText = pokemonNome;
  elemento.querySelector(".arroba").innerText = `@${("00" + pokemonNum).slice(
    -3
  )}-${pokemonNome}`;
  return elemento;
}
